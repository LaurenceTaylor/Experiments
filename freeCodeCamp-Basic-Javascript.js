myVar++;
// this is equivalent to:
myVar = myVar + 1;

myVar--;
// this is equivalent to:
myVar = myVar - 1;

// % divides and gives the remainder
// 5 % 2 = 1
// 17 % 2 = 1 (17 is Odd)
// 48 % 2 = 0 (48 is Even)
myVar = 11 % 3;

